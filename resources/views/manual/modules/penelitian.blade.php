<p class="lead">Modul <strong>Penelitian</strong> mengelola siklus proposal penelitian dari draft hingga keputusan akhir LPPM.</p>

<h2>1. Alur standar</h2>
<ol>
    <li><strong>Draft</strong> — dosen/admin menyusun proposal dan lampiran.</li>
    <li><strong>Diajukan</strong> — proposal masuk antrian admin LPPM.</li>
    <li><strong>Verifikasi admin</strong> — kelengkapan dokumen dan RAB.</li>
    <li><strong>Review</strong> — reviewer menilai (jika skema mensyaratkan).</li>
    <li><strong>Keputusan ketua LPPM</strong> — disetujui / revisi / ditolak.</li>
</ol>

<h2>2. Membuat proposal (dosen)</h2>
<ol>
    <li>Menu <strong>Penelitian</strong> → <strong>Buat proposal</strong>.</li>
    <li>Pilih skema, judul, ketua peneliti, anggota, dan metadata wajib.</li>
    <li>Isi <strong>RAB</strong>; baris tanpa nama item diabaikan saat simpan.</li>
    <li>Unggah lampiran PDF sesuai kategori dokumen.</li>
    <li>Simpan draft, periksa ringkasan error di atas form jika ada.</li>
    <li>Klik <strong>Ajukan</strong> jika sudah lengkap.</li>
</ol>

<h2>3. Admin LPPM</h2>
<ul>
    <li>Memverifikasi proposal di antrian admin.</li>
    <li>Menugaskan reviewer jika diperlukan.</li>
    <li>Mencatat catatan verifikasi dan mengembalikan ke dosen jika kurang lengkap.</li>
</ul>

<h2>4. Reviewer</h2>
<ol>
    <li>Buka antrian review penelitian.</li>
    <li>Baca proposal dan lampiran.</li>
    <li>Isi skor/komentar dan kirim hasil review.</li>
</ol>

<h2>5. Ketua LPPM</h2>
<p>Memberi keputusan akhir berdasarkan review dan kebijakan LPPM.</p>

<h2>6. Catatan penting</h2>
<div class="note">
    Dosen hanya melihat proposal yang menjadi <strong>ketua</strong> atau sesuai scope peran.
    Admin LPPM melihat seluruh proposal institusi.
</div>
<div class="warn">
    File proposal disimpan di <code>storage/app</code> (tidak publik). Unduh hanya melalui aplikasi dengan hak akses yang sesuai.
</div>
