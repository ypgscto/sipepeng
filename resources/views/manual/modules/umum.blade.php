<p class="lead">Panduan dasar menggunakan SiPepeng: login via SIAKAD-GS, navigasi, dashboard, dan notifikasi.</p>

<h2>1. Login</h2>
<ol>
    <li>Buka halaman <strong>Masuk</strong> (<code>/login</code>).</li>
    <li>Masukkan <strong>email atau username SIAKAD-GS</strong> dan <strong>password</strong> yang sama dengan portal akademik.</li>
    <li>Klik <strong>Masuk</strong>.</li>
</ol>
<div class="warn">
    <strong>Belum bisa login?</strong> Akun harus diaktifkan oleh admin LPPM (<code>is_allowed_login</code>) dan memiliki peran yang sesuai.
    Hubungi administrator LPPM jika muncul pesan akun belum diaktifkan.
</div>

<h2>2. Navigasi</h2>
<ul>
    <li>Menu modul ada di <strong>sidebar kiri</strong> (desktop) atau tombol menu (layar kecil).</li>
    <li>Menu yang tampil mengikuti <strong>peran (role)</strong> akun Anda.</li>
    <li>Menu <strong>Panduan / SOP</strong> berisi petunjuk per modul (dokumen ini).</li>
</ul>

<h2>3. Dashboard</h2>
<p>Menampilkan ringkasan statistik penelitian, PkM, publikasi, HKI, dan dana. Filter tahun akademik, prodi, dan dosen <strong>otomatis dibatasi</strong> sesuai peran Anda.</p>

<h2>4. Notifikasi</h2>
<ol>
    <li>Klik ikon <strong>bell</strong> di pojok kanan atas.</li>
    <li>Baca notifikasi terkait proposal, review, atau keputusan.</li>
    <li>Gunakan tautan aksi untuk membuka halaman terkait.</li>
</ol>

<h2>5. Keluar (logout)</h2>
<p>Gunakan menu profil → <strong>Keluar</strong>. Tutup browser jika memakai komputer bersama.</p>

