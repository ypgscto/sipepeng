<p class="lead">Modul <strong>Pengabdian Masyarakat (PkM)</strong> mengelola proposal pengabdian dengan alur serupa modul penelitian.</p>

<h2>1. Alur standar</h2>
<ol>
    <li>Draft proposal PkM oleh dosen/ketua tim.</li>
    <li>Pengajuan ke admin LPPM.</li>
    <li>Verifikasi kelengkapan dokumen dan anggaran.</li>
    <li>Review (jika diperlukan skema).</li>
    <li>Keputusan ketua LPPM.</li>
</ol>

<h2>2. Menyusun proposal PkM</h2>
<ol>
    <li>Buka <strong>Pengabdian Masyarakat</strong> → buat proposal baru.</li>
    <li>Isi judul, skema PkM, lokasi/mitra, dan anggota tim.</li>
    <li>Lengkapi RAB dan lampiran wajib (PDF).</li>
    <li>Simpan sebagai draft; perbaiki jika ada pesan validasi.</li>
    <li>Ajukan proposal setelah yakin lengkap.</li>
</ol>

<h2>3. Peran dalam modul</h2>
<table>
    <thead>
        <tr>
            <th>Peran</th>
            <th>Tugas</th>
        </tr>
    </thead>
    <tbody>
        <tr>
            <td>Dosen</td>
            <td>Membuat dan mengajukan proposal sebagai ketua tim</td>
        </tr>
        <tr>
            <td>Admin LPPM</td>
            <td>Verifikasi administrasi dan koordinasi review</td>
        </tr>
        <tr>
            <td>Reviewer</td>
            <td>Menilai kelayakan proposal yang ditugaskan</td>
        </tr>
        <tr>
            <td>Ketua LPPM</td>
            <td>Keputusan akhir penerimaan pendanaan</td>
        </tr>
    </tbody>
</table>

<h2>4. Dokumen dan mitra</h2>
<p>Lampirkan surat kerjasama atau dokumen mitra jika disyaratkan skema. Data mitra dapat dikaitkan dengan master <strong>jenis mitra</strong> di Data Master.</p>

<h2>5. Setelah disetujui</h2>
<p>Gunakan modul <strong>Surat LPPM</strong> untuk menerbitkan surat tugas/izin PkM jika diperlukan kebijakan kampus.</p>
