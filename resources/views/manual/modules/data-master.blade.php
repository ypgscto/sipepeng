<p class="lead">Modul <strong>Data Master</strong> mengelola referensi LPPM yang dipakai di seluruh transaksi: skema, kategori dokumen, reviewer, template, dan lainnya.</p>

<h2>1. Hak akses</h2>
<p>Super admin, admin LPPM, dan ketua LPPM.</p>

<h2>2. Entitas master utama</h2>
<ul>
    <li><strong>Skema penelitian</strong> dan <strong>skema pengabdian</strong> — jenis pendanaan/program</li>
    <li><strong>Kategori dokumen</strong> — klasifikasi lampiran wajib</li>
    <li><strong>Template dokumen</strong> — formulir unduhan (PDF/DOC)</li>
    <li><strong>Reviewer</strong> — daftar penilai eksternal/internal</li>
    <li><strong>Klaster ilmu, fokus riset, sumber dana, jenis luaran</strong> — metadata proposal</li>
    <li><strong>Jenis HKI, jenis publikasi, jenis surat, status proposal</strong></li>
</ul>

<h2>3. Menambah / mengubah data master</h2>
<ol>
    <li>Buka <strong>Data Master</strong> → pilih kartu entitas yang diinginkan.</li>
    <li>Klik <strong>Tambah</strong> atau edit baris yang ada.</li>
    <li>Isi field wajib (bertanda *) lalu <strong>Simpan</strong>.</li>
</ol>
<div class="warn">
    Menonaktifkan atau menghapus master yang sudah dipakai proposal dapat memengaruhi tampilan data lama.
    Lebih aman gunakan status <em>nonaktif</em> jika tersedia.
</div>

<h2>4. Skema penelitian / pengabdian</h2>
<p>Setiap skema dapat memiliki <strong>URL panduan</strong> (tautan eksternal) yang muncul di form proposal dosen.</p>

<h2>5. Template dokumen</h2>
<ol>
    <li>Unggah file template (PDF/DOC) melalui master <strong>Template Dokumen</strong>.</li>
    <li>Dosen mengunduh template dari form proposal atau halaman pengaturan template.</li>
</ol>

<h2>6. SOP persiapan semester baru</h2>
<ol>
    <li>Review skema aktif dan periode pendanaan.</li>
    <li>Perbarui kategori dokumen wajib jika kebijakan LPPM berubah.</li>
    <li>Daftarkan reviewer baru jika diperlukan.</li>
    <li>Refresh referensi SIAKAD sebelum pembukaan pengajuan proposal.</li>
</ol>
