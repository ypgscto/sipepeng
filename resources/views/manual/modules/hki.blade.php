<p class="lead">Modul <strong>HKI dan Paten</strong> mengelola pendaftaran kekayaan intelektual dan antrian verifikasi LPPM.</p>

<h2>1. Alur pendaftaran HKI</h2>
<ol>
    <li>Dosen/pemohon membuat pendaftaran HKI baru.</li>
    <li>Mengisi jenis HKI, judul ciptaan, inventors, dan tanggal.</li>
    <li>Mengunggah dokumen pendukung (berkas permohonan, gambar, dll.).</li>
    <li>Mengajukan ke antrian verifikasi admin LPPM.</li>
    <li>Admin memverifikasi kelengkapan dan status.</li>
</ol>

<h2>2. Antrian admin</h2>
<p>Admin LPPM membuka antrian <strong>Verifikasi Admin</strong> untuk:</p>
<ul>
    <li>Memeriksa dokumen yang diunggah.</li>
    <li>Mengubah status verifikasi.</li>
    <li>Memberi catatan jika dokumen perlu dilengkapi.</li>
</ul>

<h2>3. Jenis HKI</h2>
<p>Jenis HKI dikelola di <strong>Data Master → Jenis HKI</strong>. Pastikan jenis yang dipakai masih aktif sebelum membuka periode pendaftaran baru.</p>

