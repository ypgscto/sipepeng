<p class="lead">Modul <strong>Publikasi Ilmiah</strong> mencatat karya publikasi dosen dan proses verifikasi LPPM.</p>

<h2>1. Alur</h2>
<ol>
    <li>Dosen membuat entri publikasi (draft).</li>
    <li>Melengkapi metadata: judul, jurnal/prosiding, tahun, penulis, DOI/link.</li>
    <li>Mengunggah bukti publikasi (PDF) jika diwajibkan.</li>
    <li>Mengajukan ke admin LPPM untuk verifikasi.</li>
    <li>Admin memverifikasi atau meminta perbaikan.</li>
</ol>

<h2>2. Membuat publikasi</h2>
<ol>
    <li>Menu <strong>Publikasi Ilmiah</strong> → <strong>Tambah</strong>.</li>
    <li>Pilih jenis publikasi dari master data.</li>
    <li>Isi semua field wajib.</li>
    <li>Simpan dan ajukan jika sudah final.</li>
</ol>

<h2>3. Admin LPPM</h2>
<ul>
    <li>Memeriksa kesesuaian bukti dan metadata.</li>
    <li>Menyetujui atau mengembalikan ke dosen.</li>
    <li>Data terverifikasi masuk laporan LPPM.</li>
</ul>

<h2>4. Laporan</h2>
<p>Publikasi terverifikasi tampil di modul <strong>Laporan</strong> dan dapat diekspor sesuai hak akses peran.</p>

<div class="note">
    Dosen hanya mengelola publikasi yang mereka ajukan. Admin LPPM melihat seluruh data institusi.
</div>
