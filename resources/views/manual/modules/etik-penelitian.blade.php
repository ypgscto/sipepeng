<p class="lead">Modul <strong>Etik Penelitian</strong> mengelola pengajuan persetujuan etik untuk penelitian yang melibatkan manusia atau data sensitif.</p>

<h2>1. Kapan mengajukan etik</h2>
<p>Ajukan jika penelitian memerlukan persetujuan komisi etik sesuai kebijakan LPPM/kampus, misalnya penelitian pada subjek manusia, data kesehatan, atau kuesioner sensitif.</p>

<h2>2. Alur pengajuan</h2>
<ol>
    <li>Buat pengajuan etik baru dari menu <strong>Etik Penelitian</strong>.</li>
    <li>Kaitkan dengan proposal penelitian jika tersedia.</li>
    <li>Unggah protokol penelitian, informed consent, dan lampiran wajib.</li>
    <li>Ajukan ke admin LPPM.</li>
    <li>Admin memverifikasi dan mencatat keputusan etik.</li>
</ol>

<h2>3. Admin LPPM</h2>
<ul>
    <li>Memeriksa kelengkapan dokumen etik.</li>
    <li>Mengelola antrian verifikasi.</li>
    <li>Mencatat nomor/tanggal persetujuan etik jika disetujui.</li>
</ul>

<h2>4. Dokumen wajib (umum)</h2>
<ul>
    <li>Protokol penelitian</li>
    <li>Lembar persetujuan peserta (informed consent)</li>
    <li>Kuesioner / instrumen penelitian</li>
    <li>Surat pengantar prodi (jika disyaratkan)</li>
</ul>

<div class="note">
    Format file mengikuti kategori dokumen di Data Master. Gunakan PDF untuk lampiran utama.
</div>
