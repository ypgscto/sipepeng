<p class="lead">Modul <strong>Laporan</strong> menyajikan rekap data LPPM dengan filter dan opsi export Excel/PDF.</p>

<h2>1. Hak akses laporan</h2>
<table>
    <thead>
        <tr>
            <th>Peran</th>
            <th>Cakupan data</th>
        </tr>
    </thead>
    <tbody>
        <tr>
            <td>Super admin, Admin LPPM, Ketua LPPM, Pimpinan</td>
            <td>Seluruh institusi</td>
        </tr>
        <tr>
            <td>Ketua prodi</td>
            <td>Data prodi sendiri</td>
        </tr>
        <tr>
            <td>Dosen</td>
            <td>Penelitian, PkM, publikasi, HKI, etik milik sendiri</td>
        </tr>
    </tbody>
</table>

<h2>2. Menggunakan laporan</h2>
<ol>
    <li>Buka menu <strong>Laporan</strong> (atau dari dashboard).</li>
    <li>Pilih jenis laporan: penelitian, PkM, publikasi, HKI, dana, dll.</li>
    <li>Terapkan filter: tahun akademik, prodi, status, skema.</li>
    <li>Lihat grafik dan tabel ringkasan.</li>
</ol>

<h2>3. Export</h2>
<ol>
    <li>Klik <strong>Export Excel</strong> atau <strong>Export PDF</strong> jika tombol tersedia.</li>
    <li>Export dibatasi maksimal <strong>5000 baris</strong> per permintaan.</li>
    <li>Persempit filter jika data terlalu besar.</li>
</ol>

<h2>4. Dashboard vs laporan lengkap</h2>
<p>Dashboard menampilkan ringkasan cepat. Modul laporan menyediakan filter lebih detail dan export resmi untuk pelaporan LPPM ke pimpinan.</p>

