<p class="lead">Modul <strong>Surat LPPM</strong> membuat surat resmi (tugas, izin, undangan reviewer, dll.) dengan nomor surat dan PDF.</p>

<h2>1. Jenis surat</h2>
<p>Jenis surat dikonfigurasi di Data Master (<strong>Jenis Surat</strong>) dan template Blade di sistem. Contoh umum:</p>
<ul>
    <li>Surat tugas penelitian / PkM</li>
    <li>Surat izin penelitian / pengabdian</li>
    <li>Surat undangan reviewer / seminar</li>
    <li>Surat keterangan selesai penelitian / PkM</li>
</ul>

<h2>2. Membuat surat</h2>
<ol>
    <li>Buka <strong>Surat LPPM</strong> → <strong>Buat surat</strong>.</li>
    <li>Pilih jenis surat dan kaitkan proposal/partner jika diperlukan.</li>
    <li>Isi penerima, tanggal, tempat, dan isi surat.</li>
    <li>Simpan sebagai draft.</li>
    <li>Preview PDF sebelum diterbitkan.</li>
</ol>

<h2>3. Penerbitan surat</h2>
<ol>
    <li>Setelah disetujui internal, ubah status ke <strong>Diterbitkan</strong>.</li>
    <li>Sistem menggenerate nomor surat (sesuai konfigurasi).</li>
    <li>Unduh PDF surat resmi.</li>
    <li>Unggah surat bertanda tangan jika workflow mensyaratkan upload balik.</li>
</ol>

<h2>4. Draft vs terbit</h2>
<div class="warn">
    PDF draft dapat memuat watermark <strong>DRAFT</strong>. Surat terbit menggunakan nomor resmi dan disimpan di storage aplikasi.
</div>

<h2>5. Hak akses</h2>
<p>Dosen dapat membuat surat terkait proposal sendiri. Admin LPPM mengelola seluruh surat institusi.</p>
